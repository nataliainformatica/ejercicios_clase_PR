public class Ejercicio3 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int num = 100;
        while (num > 0) {
            System.out.println(num);
            num--;
        }

        num = 100;
        while (num > 0) {
            System.out.print(num+" ");
            num--;
        }
        
        System.out.println("\nFin de programa");
    }

}
